// Za konvertovanje niza u string korisitimo metodu toString()

const fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits.toString());

// pop() metoda sklanja poslednji element u nizu

const fruits1 = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits1.pop());

// push() metoda dodaje novi element na zadnje mesto niza
const fruits3 = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits3.push("Kiwi"));

// shift() metoda sklanja prvi element niza

const fruits4 = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits4.shift());

// unshift() metoda dodaje novi element na prvo mesto

const fruits5 = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits5.unshift("Lemon"));

