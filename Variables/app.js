// U JavaScriptu postoji 3 nacina da deklarisemo varijablu

// 1. nacin je koriscenjem keyword-a var
// **NAPOMENA** varijable deklarisane koriscenjem var keyword-a mogu biti
// redeklarisane i reinicijalizovane
var prom;
prom = 12;
// 2. nacin je koriscenjem keyword-a let
// **NAPOMENA** varijable deklarisane koriscenjem let keyword-a mogu biti
// reinicijalizovane, a ne mogu biti redeklarisane (osim ako nisu u drugom scope-u)
let prom1;
prom1 = 12;
// 3. nacin je koriscenjem const keyword-a
// **NAPOMENA** varijable deklarisane const kljucnom reci ne mogu biti ni reinicijalizovane
// ni redeklarisane
const x = 12;

